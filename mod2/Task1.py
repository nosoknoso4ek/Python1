# Ввод данных из консоли
input_data = input()  # Считываем строку, например, "123, 12"

# Разделяем строку на две части по запятой
a_str, b_str = input_data.split(',')

# Преобразуем строки в целые числа
a = int(a_str.strip())  # Убираем лишние пробелы и преобразуем в int
b = int(b_str.strip())  # Убираем лишние пробелы и преобразуем в int

# Вычисляем остаток от деления
remainder = a % b

# Выводим результат
print(remainder)