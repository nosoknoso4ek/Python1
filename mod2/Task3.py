# Ввод трех чисел из консоли
input_data = input()  # Считываем строку, например, "3 1 2"

# Разделяем строку на три части по пробелам
a_str, b_str, c_str = input_data.split(' ')

# Преобразуем строки в целые числа
a = int(a_str)
b = int(b_str)
c = int(c_str)

# Упорядочиваем числа по возрастанию
if a > b:
    a, b = b, a  # Меняем местами a и b, если a > b
if b > c:
    b, c = c, b  # Меняем местами b и c, если b > c
if a > b:
    a, b = b, a  # Снова проверяем a и b, так как после предыдущих шагов они могли измениться

# Выводим среднее число (b)
print(b)