# Ввод строки и символа из консоли в одну строку через запятую
input_data = input().strip()  # Считываем строку, например, "aaabbaa,a"

# Разделяем строку на две части по запятой
s = input_data.split(',')[0]  # Первая часть — строка
i = input_data.split(',')[1]  # Вторая часть — символ

# Счетчик для количества символов i в начале строки
count = 0

# Проходим по строке с начала
index = 0
while index < len(s) and s[index] == i:
    count += 1
    index += 1

# Вывод результата
print(count)